C*
      FUNCTION EPSTIN(CLAT,RD,HAT,S1,S2,XMIN,XMAX)
C
C EVALUATE EPSTEIN FUNCTION
      D=CLAT-HAT
      EX=EXP(D)
      XL=(1.-S1/S2*EX)/(1.-S1/S2)
      XL=ALOG(XL)
      EP=RD+S1*D+(S2-S1)*XL
C
      IF (CLAT.LT.HAT.AND.EP.LT.XMIN) EP=XMIN-.000001
      IF (CLAT.GT.HAT.AND.EP.LT.XMAX) EP=XMAX-.000001
C
      EPST=EP
      RETURN
