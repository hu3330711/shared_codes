      IF( IFIRST.EQ.0 ) THEN
       COSA = COSD(ANGLE)
       SINA = SIND(ANGLE)
       IFIRST = 1
      ENDIF
C
C  ROTATE UNIT VECTOR
      X = COSA * COSD( (GMLT1-DMLT-12.)*15. ) * COSD (GMLAT1)
     1    + SINA * SIND( GMLAT1 )
      Y = SIND( (GMLT1-DMLT-12.)*15. ) * COSD( GMLAT1 )
      Z = -SINA * COSD( (GMLT1-DMLT-12.)*15. ) * COSD(GMLAT1)
     1    + COSA * SIND( GMLAT1 )
C
C  RETRIEVE COORDINATES
