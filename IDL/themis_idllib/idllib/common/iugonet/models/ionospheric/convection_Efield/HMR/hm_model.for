C                       TOWARD MIDNIGHT                                *
C                                                                      *
C   INPUTS - CALLING SEQUENCE                                          *
C     GMLAT1 - GEOMAGNETIC LATITUDE TO BE USED FOR CALCULATIONS (DEGREE)
